    private void btntambahjuiceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahjuiceActionPerformed
        if (btntambahjuice.getText().equalsIgnoreCase("Tambah")){
            btntambahjuice.setText("Batal");
            siapIsi(true);
            bersih();
            hiden();
            tampiltanggal();
            buatNoNota();
            nmpembeli.requestFocus();
            btntambah.setEnabled(true);
            btnkurang.setEnabled(true);
            tgltransaksi.setEnabled(false);
        }else{
            bersih();
            siapIsi(false);
            btntambahjuice.setText("Tambah");
            tombolNormal();
            bersih();
            tstok.setText(null);
            tgltransaksi.setText(null);
        }
    }//GEN-LAST:event_btntambahjuiceActionPerformed

