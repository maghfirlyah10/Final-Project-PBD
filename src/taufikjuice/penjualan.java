    private void buatNoNota(){
        ListDb=bc.tampil();
        if(ListDb.isEmpty()){
            tstok.setText("N-"+1);
        }else{
            int a= ListDb.size()-1;
            int no=Integer.parseInt(ListDb.get(a).getnonota().replace("N-", ""))+1;
            tstok.setText("No-"+no);
            tstok.setEnabled(false);
        }
    }
    
