    private void buatTable(){
        model = new DefaultTableModel();
        model.addColumn("Kode_Juice");
        model.addColumn("Nama_Juice");
        model.addColumn("Satuan");
        model.addColumn("Harga");
        jTable1.setModel(model);
    }
    
    private void showTable() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        ListJuice.clear();
        ListJuice = jc.tampil();
        for (x=0; x<ListJuice.size(); x++){
            Object[] data = new Object[4];
            data[0]=ListJuice.get(x).getKdjuice();
            data[1]=ListJuice.get(x).getNmjuice();
            data[2]=ListJuice.get(x).getSatuan();
            data[3]=ListJuice.get(x).getHarga();
            model.addRow(data);
        }
    }
    
    private void buatKdBarang(){
        ListJuice = jc.tampil();
        int a=ListJuice.size()-1;
        int no=Integer.parseInt(ListJuice.get(a).getKdjuice().replace("A-", ""))+1;
        kdjuice.setText("A-"+no);
        kdjuice.setEnabled(false);
    }
    
    private void siapIsi(boolean a){
        kdjuice.setEnabled(a);
        nmjuice.setEnabled(a);
        cmbsatuan.setEnabled(a);
        harga.setEnabled(a);
    }
    
    private void bersih(){
        kdjuice.setText("");
        nmjuice.setText("");
        cmbsatuan.setSelectedItem("Pilih");
        harga.setText("");
    }
    
    private void tombolNormal(){
        btnsimpan.setEnabled(false);
        btnedit.setEnabled(false);
        btnhapus.setEnabled(false);
    }
    
    private void tombolKembali(){
        btnsimpan.setEnabled(true);
        btnhapus.setEnabled(true);
    }
    
    public void tampilx() throws SQLException{
        int jmljuice=jc.getJumlahjuice();
        txtjml.setText(String.valueOf(jmljuice));
    }


