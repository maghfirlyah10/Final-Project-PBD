    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        if (nmjuice.getText().isEmpty() || cmbsatuan.getSelectedItem().equals("")
            || harga.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Data Harus Lengkap", "Aplikasi Juice Taufik", JOptionPane.INFORMATION_MESSAGE);
        }else{
            DJ.setKdjuice(kdjuice.getText());
            DJ.setNmjuice(nmjuice.getText());
            DJ.setSatuan(cmbsatuan.getSelectedItem().toString());
            DJ.setHarga(Integer.parseInt(harga.getText()));
            if(btntambah.getText().equalsIgnoreCase("Batal")){
                if (jc.tambahjuice(DJ)==1){
                    JOptionPane.showMessageDialog(null,"Data Telah Di Simpan", "Aplikasi Juice Taufik", JOptionPane.INFORMATION_MESSAGE );
                    buatTable();
                    showTable();
                    try {
                        tampilx();
                    } catch (SQLException ex) {
                        Logger.getLogger(datajuice.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else{
                    JOptionPane.showMessageDialog(null,"Data Gagal Di Simpan", "Aplikasi Juice Taufik", JOptionPane.INFORMATION_MESSAGE );
                }
            }
            if (btnedit.getText().equalsIgnoreCase("Batal")){
                if (jc.editJuice(DJ)==1){
                    JOptionPane.showMessageDialog(null,"Data Telah Dirubah", "Aplikasi Juice Taufik", JOptionPane.INFORMATION_MESSAGE );
                    buatTable();
                    showTable();
                }else{
                    JOptionPane.showMessageDialog(null,"Data Gagal Dirubah", "Aplikasi Juice Taufik", JOptionPane.INFORMATION_MESSAGE );
                }
            }
            bersih();
            siapIsi(false);
            btntambah.setText("Tambah");
            btnedit.setText("Edit");
            tombolNormal();
        }

    }//GEN-LAST:event_btnsimpanActionPerformed

    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
        if (btntambah.getText().equalsIgnoreCase("Tambah")){
            btntambah.setText("Batal");
            bersih();
            siapIsi(true);
            buatKdBarang();
            nmjuice.requestFocus();
            tombolKembali();
        }else{
            btntambah.setText("tambah");
            bersih();
            siapIsi(false);
            tombolNormal();
        }
    }//GEN-LAST:event_btntambahActionPerformed

    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        if (btnedit.getText().equalsIgnoreCase("Edit")){
            btnedit.setText("Batal");
            siapIsi(true);
            kdjuice.setEnabled(false);
            nmjuice.requestFocus();
            btntambah.setEnabled(false);
            btnsimpan.setEnabled(true);
            btnhapus.setEnabled(false);
            btnedit.setEnabled(true);
        }else{
            btnedit.setText("Edit");
            bersih();
            siapIsi(false);
            tombolNormal();
        }
    }//GEN-LAST:event_btneditActionPerformed

    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        DJ.setKdjuice(kdjuice.getText());
        int pesan = JOptionPane.showConfirmDialog(null,"Data akan dihapus?","Konfirmasi",
            JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if (jc.deleteJuice(DJ)==1){
            JOptionPane.showMessageDialog(null, "Data telah dihapus");
            bersih();
            siapIsi(false);
            tombolNormal();
            buatTable();
            showTable();
            try {
                tampilx();
            } catch (SQLException ex) {
                Logger.getLogger(datajuice.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Data gagal disimpan");
        }
    }//GEN-LAST:event_btnhapusActionPerformed


